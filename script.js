// ── Event Listeners ────────────────────────────────────
function initEventListeners() {
  // Navbar scroll
  window.addEventListener('scroll', () => {
    navbar.classList.toggle('scrolled', window.scrollY > 20);
  });

  // Mobile menu
  mobileMenuBtn.addEventListener('click', () => {
    mobileMenuBtn.classList.toggle('active');
    mobileMenu.classList.toggle('open');
  });

  // Services dropdown
  servicesDropdown.addEventListener('click', (e) => {
    e.preventDefault();
    servicesDropdown.parentElement.classList.toggle('open');
  });
  document.addEventListener('click', (e) => {
    if (!servicesDropdown.parentElement.contains(e.target)) {
      servicesDropdown.parentElement.classList.remove('open');
    }
  });

  // Language toggle
  langToggle.addEventListener('click', () => {
    currentLanguage = currentLanguage === 'en' ? 'ar' : 'en';
    localStorage.setItem('valet-language', currentLanguage);
    applyLanguage(currentLanguage);
  });

  // Theme toggle
  themeToggle.addEventListener('click', () => {
    currentTheme = currentTheme === 'light' ? 'dark' : 'light';
    localStorage.setItem('valet-theme', currentTheme);
    applyTheme(currentTheme);
  });

  // Scroll to top
  if (scrollTopBtn) {
    scrollTopBtn.addEventListener('click', () => {
      window.scrollTo({ top: 0, behavior: 'smooth' });
    });
  }

  // Close mobile menu on link click
  document.querySelectorAll('.mobile-link').forEach(link => {
    link.addEventListener('click', () => {
      mobileMenuBtn.classList.remove('active');
      mobileMenu.classList.remove('open');
    });
  });

  // Smooth scroll for anchor links
  document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', (e) => {
      const href = anchor.getAttribute('href');
      if (href !== '#') {
        e.preventDefault();
        const target = document.querySelector(href);
        if (target) target.scrollIntoView({ behavior: 'smooth' });
      }
    });
  });

  // "Book Now" buttons on service cards open booking modal with pre-selected service
  document.querySelectorAll('.service-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      const svc = btn.dataset.service;
      if (svc) {
        const sel = document.getElementById('bkService');
        if (sel) sel.value = svc;
      }
      openModal(bookingModal);
    });
  });

  // Hero / nav "Book Now" buttons
  ['navBookNow', 'mobileBookNow', 'heroBookNow'].forEach(id => {
    const el = document.getElementById(id);
    if (el) el.addEventListener('click', () => openModal(bookingModal));
  });
}

// ── Auth Modals ────────────────────────────────────────
function initAuthModals() {
  if (openLoginBtn) {
    openLoginBtn.addEventListener('click', (e) => {
      e.preventDefault();
      openModal(loginModal);
    });
  }
  if (openLoginBtnMobile) {
    openLoginBtnMobile.addEventListener('click', (e) => {
      e.preventDefault();
      mobileMenuBtn.classList.remove('active');
      mobileMenu.classList.remove('open');
      openModal(loginModal);
    });
  }

  closeLoginModal.addEventListener('click',  () => closeModal(loginModal));
  closeSignupModal.addEventListener('click', () => closeModal(signupModal));

  showSignup.addEventListener('click', (e) => {
    e.preventDefault();
    closeModal(loginModal);
    setTimeout(() => openModal(signupModal), 200);
  });
  showLogin.addEventListener('click', (e) => {
    e.preventDefault();
    closeModal(signupModal);
    setTimeout(() => openModal(loginModal), 200);
  });

  loginModal.addEventListener('click',  (e) => { if (e.target === loginModal)  closeModal(loginModal);  });
  signupModal.addEventListener('click', (e) => { if (e.target === signupModal) closeModal(signupModal); });

  document.addEventListener('keydown', (e) => {
    if (e.key === 'Escape') { closeModal(loginModal); closeModal(signupModal); closeModal(bookingModal); }
  });

  // Password visibility toggle
  document.querySelectorAll('.toggle-password').forEach(btn => {
    btn.addEventListener('click', () => {
      const input = btn.previousElementSibling;
      input.setAttribute('type', input.type === 'password' ? 'text' : 'password');
      btn.classList.toggle('active');
    });
  });

  // Login form → PHP API
  loginForm.addEventListener('submit', async (e) => {
    e.preventDefault();
    const errEl = document.getElementById('loginError');
    errEl.style.display = 'none';
    const btn   = loginForm.querySelector('[type=submit]');
    const email    = document.getElementById('loginEmail').value.trim();
    const password = document.getElementById('loginPassword').value;
    if (!email || !password) { showApiError(errEl, 'Please fill in all fields.'); return; }
    setBtn(btn, true);
    try {
      const res = await apiPost('api/auth/login.php', { email, password });
      if (res.success) {
        showToast('Welcome back, ' + res.user.full_name + '!', 'success');
        setTimeout(() => location.reload(), 700);
      } else {
        showApiError(errEl, res.message);
      }
    } catch { showApiError(errEl, 'Connection error. Is XAMPP running?'); }
    finally   { setBtn(btn, false); }
  });

  // Signup form → PHP API
  signupForm.addEventListener('submit', async (e) => {
    e.preventDefault();
    const errEl = document.getElementById('signupError');
    errEl.style.display = 'none';
    const btn      = signupForm.querySelector('[type=submit]');
    const full_name = document.getElementById('signupName').value.trim();
    const email     = document.getElementById('signupEmail').value.trim();
    const phone     = document.getElementById('signupPhone').value.trim();
    const password  = document.getElementById('signupPassword').value;
    const terms     = document.getElementById('signupTerms').checked;
    if (!full_name || !email || !phone || !password) { showApiError(errEl, 'Please fill in all fields.'); return; }
    if (password.length < 8) { showApiError(errEl, 'Password must be at least 8 characters.'); return; }
    if (!terms) { showApiError(errEl, 'Please agree to the Terms of Service.'); return; }
    setBtn(btn, true);
    try {
      const res = await apiPost('api/auth/register.php', { full_name, email, phone, password });
      if (res.success) {
        showToast('Account created! Welcome to VALET!', 'success');
        setTimeout(() => location.reload(), 700);
      } else {
        showApiError(errEl, res.message);
      }
    } catch { showApiError(errEl, 'Connection error. Is XAMPP running?'); }
    finally   { setBtn(btn, false); }
  });
}

// ── Booking Modal ──────────────────────────────────────
function initBookingModal() {
  if (!bookingModal) return;
  closeBookingModal.addEventListener('click', () => closeModal(bookingModal));
  bookingModal.addEventListener('click', (e) => { if (e.target === bookingModal) closeModal(bookingModal); });

  bookingForm.addEventListener('submit', async (e) => {
    e.preventDefault();
    const errEl = document.getElementById('bookingError');
    errEl.style.display = 'none';
    const btn = document.getElementById('bkSubmit');

    const full_name      = document.getElementById('bkName').value.trim();
    const email          = document.getElementById('bkEmail').value.trim();
    const phone          = document.getElementById('bkPhone').value.trim();
    const service_type   = document.getElementById('bkService').value;
    const preferred_date = document.getElementById('bkDate').value;
    const preferred_time = document.getElementById('bkTime').value;
    const address        = document.getElementById('bkAddress').value.trim();
    const notes          = document.getElementById('bkNotes').value.trim();

    if (!full_name || !email || !phone || !service_type || !preferred_date || !preferred_time || !address) {
      showApiError(errEl, 'Please fill in all required fields.');
      return;
    }
    setBtn(btn, true);
    try {
      const res = await apiPost('api/bookings/create.php', { full_name, email, phone, service_type, preferred_date, preferred_time, address, notes });
      if (res.success) {
        bookingForm.style.display = 'none';
        document.getElementById('bookingSuccess').style.display = 'block';
        showToast('Booking confirmed! We will contact you shortly.', 'success');
        setTimeout(() => {
          closeModal(bookingModal);
          bookingForm.style.display = '';
          document.getElementById('bookingSuccess').style.display = 'none';
          bookingForm.reset();
        }, 3000);
      } else {
        showApiError(errEl, res.message);
      }
    } catch { showApiError(errEl, 'Connection error. Is XAMPP running?'); }
    finally   { setBtn(btn, false); }
  });
}

